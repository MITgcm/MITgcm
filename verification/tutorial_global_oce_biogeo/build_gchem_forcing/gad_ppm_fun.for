C Hack to get rid of negatives caused by Redi.  Works by restricting the
C outgoing flux (only contributions computed in gad_calc_rhs) for each cell
C to be no more than the amount of tracer in the cell (see Smolarkiewicz
C MWR 1989 and Bott MWR 1989).
C The flux contributions computed in gad_calc_rhs which are affected by
C this hack are:
C - explicit diffusion, Redi and the non-local part of KPP
C - advection is affected only if multiDimAdvection=.FALSE.
C - vertical diffusion (including the diagonal contribution from GMRedi)
C   only if implicitDiffusion=.FALSE.
C - GM is affected only if GMREDI_AdvForm=.FALSE.
C
C The parameter SmolarkiewiczMaxFrac (defined in gad_init_fixed.F)
C specifies the maximal fraction of tracer that can leave a cell.
C By default it is 1.  This will prevent the tracer from going negative
C due to contributions from gad_calc_rhs alone.  In the presence of other
C contributions (or roundoff errors), it may be necessary to reduce this
C value to achieve strict positivity.
C
C This hack applies to all tracers except temperature and salinity!
C Do not use with Adams-Bashforth (for ptracers)!
C Do not use with OBCS!




C--  File gad_ppm_fun.F: Routines to form PPM grid-cell polynomial.
C--   Contents
C--   o GAD_PPM_FUN_NULL
C--   o GAD_PPM_FUN_MONO

C---+----1----+----2----+----3----+----4----+----5----+----6----+----7-|--+----|

      SUBROUTINE GAD_PPM_FUN_NULL(
     I           ff00,
     I           fell,ferr,
     O           fhat,mono)
C     |================================================================|
C     | PPM_FUN_NULL: form PPM grid-cell polynomial.                   |
C     | Piecewise Parabolic Method (PPM), unlimited variant.           |
C     |================================================================|

          implicit none

C     ====================================================== arguments
          Real*8 ff00
          Real*8 fell,ferr
          Real*8 fhat(+1:+3)
          integer  mono

          mono = +0

C     ============================================== unlimited profile
          fhat( 1 ) =
     &  +(3.D0 / 2.D0) * ff00
     &  -(1.D0 / 4.D0) *(ferr+fell)
          fhat( 2 ) =
     &  +(1.D0 / 2.D0) *(ferr-fell)
          fhat( 3 ) =
     &  -(3.D0 / 2.D0) * ff00
     &  +(3.D0 / 4.D0) *(ferr+fell)

          return

c     end subroutine GAD_PPM_FUN_NULL
      end

C---+----1----+----2----+----3----+----4----+----5----+----6----+----7-|--+----|

      SUBROUTINE GAD_PPM_FUN_MONO(
     I           ff00,
     I           ffll,ffrr,
     I           fell,ferr,
     I           dfds,
     O           fhat,mono)
C     |================================================================|
C     | PPM_FUN_MONO: form PPM grid-cell polynomial.                   |
C     | Piecewise Parabolic Method (PPM) - monotonic variant.          |
C     |================================================================|

          implicit none

C     ====================================================== arguments
          Real*8 ff00
          Real*8 ffll,ffrr
          Real*8 fell,ferr
          Real*8 dfds(-1:+1)
          Real*8 fhat(+1:+3)
          integer mono

C     ====================================================== variables
          Real*8 turn

          mono = 0

C     ============================================== "flatten" extrema
          if((ffrr-ff00) *
     &       (ff00-ffll) .le. 0.D0) then

              mono = +1

              fhat(1) = ff00

              fhat(2) = 0.D0
              fhat(3) = 0.D0

              return

          end if

C     ============================================== limit edge values
          if((ffll-fell) *
     &       (fell-ff00) .le. 0.D0) then

              mono = +1

              fell = ff00 - dfds(0)

          end if

          if((ffrr-ferr) *
     &       (ferr-ff00) .le. 0.D0) then

              mono = +1

              ferr = ff00 + dfds(0)

          end if

C     ============================================== limit cell values
          fhat( 1 ) =
     &  +(3.D0 / 2.D0) * ff00
     &  -(1.D0 / 4.D0) *(ferr+fell)
          fhat( 2 ) =
     &  +(1.D0 / 2.D0) *(ferr-fell)
          fhat( 3 ) =
     &  -(3.D0 / 2.D0) * ff00
     &  +(3.D0 / 4.D0) *(ferr+fell)

          if (abs(fhat(3)) .gt.
     &        abs(fhat(2))*.5D0) then

          turn = -0.5D0 * fhat(2)
     &                     / fhat(3)

          if ((turn .ge. -1.D0)
     &   .and.(turn .le. +0.D0)) then

              mono = +2

C     ====================================== push TURN onto lower edge
              ferr = +3.D0 * ff00
     &               -2.D0 * fell

          end if

          if ((turn .gt. +0.D0)
     &   .and.(turn .le. +1.D0)) then

              mono = +2

C     ====================================== push TURN onto upper edge
              fell = +3.D0 * ff00
     &               -2.D0 * ferr

          end if

          end if

          if (mono .gt. +1) then

C     ====================================== re-calc. coeff. on demand
          fhat( 1 ) =
     &  +(3.D0 / 2.D0) * ff00
     &  -(1.D0 / 4.D0) *(ferr+fell)
          fhat( 2 ) =
     &  +(1.D0 / 2.D0) *(ferr-fell)
          fhat( 3 ) =
     &  -(3.D0 / 2.D0) * ff00
     &  +(3.D0 / 4.D0) *(ferr+fell)

          end if

          return

c     end subroutine GAD_PPM_FUN_MONO
      end

