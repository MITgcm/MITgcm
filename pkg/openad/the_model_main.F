#endif
          CALL OpenAD_THE_MAIN_LOOP( myTime, myIter, myThid )
#ifdef ALLOW_OPENAD_DIVA
          call cp_read_state(currcp, curradjointcp, maxfwditer, maxadjit
     +er, myIter)
