      call cp_read_state(currcp, curradjointcp, maxfwditer, maxadjiter,
     +myIter)
      fwddone = .true.
      if (curradjointcp .eq. -1 .and. currcp.ne. nTimeSteps_l2) then
        fwddone = .false.
      end if
      print *, 'DIVA myIter is', myIter , nIter0
      IF (debugMode) CALL DEBUG_CALL('THE_MAIN_LOOP',myThid)
