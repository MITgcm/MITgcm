      LOGICAL fwddone
      integer currcp, curradjointcp, maxfwditer, maxadjiter
      _RL foo(1-olx:snx+olx,1-oly:sny+oly,nr,nsx,nsy)
      _RL foo2D(1-olx:snx+olx,1-oly:sny+oly,nsx,nsy)
      CHARACTER*(10) suff
      CHARACTER*(MAX_LEN_FNAM) fname
